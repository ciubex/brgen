package ro.ciubex.brgen.model;

/**
 * Model used to populate google calendar id and name.
 * 
 * @author Claudiu Ciobotariu
 * 
 */
public class GoogleCalendar {
	private long id;
	private String name;

	public GoogleCalendar(long id, String name) {
		this.id = id;
		this.name = name;
	}

	/**
	 * @return the id
	 */
	public long getId() {
		return id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
}
